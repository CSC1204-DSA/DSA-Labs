#include <stdio.h>

int main(void) {
    // Declaring variables of various data types
    int myInteger = 10;           // Integer type
    float myFloat = 3.14f;        // Float type (single precision)
    double myDouble = 3.14159265; // Double type (double precision)
    char myCharacter = 'A';       // Character type

    // Printing the values
    printf("Integer value: %d\n", myInteger);
    printf("Float value: %f\n", myFloat);
    printf("Double value: %lf\n", myDouble);
    printf("Character value: %c\n", myCharacter);

    return 0;
}
